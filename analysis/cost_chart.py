#!/usr/bin/env python3
"""Cost-per-task vs Pass@1 scatter for OmicVerse-OmicBench.

  python3 analysis/cost_chart.py

Reads `analysis/cost_summary.csv` (generated by bench_cost.py) and the
`results/<run>/grades.csv` files, then emits
`analysis/cost_vs_score.png` — cost on a log x-axis, Pass@1 on the y-axis,
each model shown as `baseline → +OmicVerse` (open dot → filled green dot)
joined by an arrow so the omicverse uplift is visible.

Cost is **cache-adjusted** (the lower-bound estimate, assuming a warm
prompt cache; see bench_cost.py). For providers where the per-call
prompt_tokens is already de-duplicated server-side (notably the Codex
OAuth bridge for gpt-5.5), naive and cache-adjusted converge.
"""
import csv
import glob
import json
import sys
from collections import defaultdict
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
from matplotlib.ticker import LogLocator, FuncFormatter

sys.path.insert(0, str(Path(__file__).resolve().parent))
from bench_cost import PRICING, summarise   # noqa: E402

PROJECT = Path(__file__).resolve().parents[1]
ANALYSIS = Path(__file__).resolve().parent

# Provider palette (Physics-Intern style):
PROVIDER = {
    "gpt-5.5":                       ("OpenAI",          "#222222"),
    "deepseek-v4-pro":               ("DeepSeek",        "#3B6FE0"),
    "deepseek-v4-flash":             ("DeepSeek",        "#3B6FE0"),
    "gemini-3.1-flash-lite-preview": ("Google · Gemini", "#4FB99F"),
    "glm-5.1":                       ("Zhipu",           "#A04EBF"),
    "MiniMax-M2.7":                  ("MiniMax",         "#E8894C"),
    "qwen3.6:35b-a3b-256k":          ("Alibaba (open)",  "#6E8B3D"),
}
OMICVERSE_RING = "#C0392B"   # red — highlights the +omicverse arm


def load_cost_rows():
    rows = []
    with open(ANALYSIS / "cost_summary.csv") as fh:
        for r in csv.DictReader(fh):
            for k in ("seed", "n_calls", "in_tok", "fresh_tok", "out_tok"):
                r[k] = int(r[k])
            rows.append(r)
    return rows


def load_grades():
    """-> {(model_id, arm): mean pass rate (0..1)}"""
    bucket = defaultdict(list)
    for f in glob.glob(str(PROJECT / "results" / "*" / "grades.csv")):
        with open(f) as fh:
            for row in csv.DictReader(fh):
                sys_ = row["system"]
                if sys_.endswith("_omicverse"):       arm = "omicverse"
                elif sys_.endswith("_baseline"):      arm = "baseline"
                elif "no_registry" in sys_:           arm = "omicverse_no_registry"
                elif "doc_rag" in sys_:               arm = "omicverse_doc_rag"
                else:                                 arm = sys_
                passed = row.get("passed", "").strip().lower()
                if passed in ("true", "1", "1.0"):
                    bucket[(row["model_id"], arm)].append(1)
                elif passed in ("false", "0", "0.0"):
                    bucket[(row["model_id"], arm)].append(0)
    return {k: sum(v) / len(v) for k, v in bucket.items() if v}


def short_label(model_id):
    return {
        "gpt-5.5":                       "gpt-5.5",
        "deepseek-v4-pro":               "DeepSeek v4-pro",
        "deepseek-v4-flash":             "DeepSeek v4-flash",
        "gemini-3.1-flash-lite-preview": "Gemini 3.1 Flash Lite",
        "glm-5.1":                       "GLM-5.1",
        "MiniMax-M2.7":                  "MiniMax-M2.7",
        "qwen3.6:35b-a3b-256k":          "Qwen3.6 35B-A3B",
    }.get(model_id, model_id)


def main():
    cost_rows = load_cost_rows()
    cost_agg = summarise(cost_rows)
    grades = load_grades()

    fig, ax = plt.subplots(figsize=(11.5, 7.5))

    legend_seen = set()
    pts_for_pareto = []

    for model_id in PRICING:
        if PRICING[model_id] == (0.0, 0.0, 0.0):
            continue   # skip local (qwen) — cost = 0 collapses to x=0 on log
        provider_name, color = PROVIDER.get(model_id, ("?", "#666"))

        b_cost = cost_agg.get((model_id, "baseline"), {}).get("cached_usd")
        o_cost = cost_agg.get((model_id, "omicverse"), {}).get("cached_usd")
        b_pass = grades.get((model_id, "baseline"))
        o_pass = grades.get((model_id, "omicverse"))

        if b_cost is None or b_pass is None:
            continue

        ax.scatter([b_cost], [b_pass * 100], s=110, marker="o",
                   facecolor="white", edgecolor=color, linewidths=2.0,
                   zorder=3,
                   label=provider_name if provider_name not in legend_seen else None)
        legend_seen.add(provider_name)
        pts_for_pareto.append((b_cost, b_pass * 100))
        ax.annotate(short_label(model_id), (b_cost, b_pass * 100),
                    textcoords="offset points", xytext=(7, -2), fontsize=8,
                    color="#555555")

        if o_cost is not None and o_pass is not None:
            ax.scatter([o_cost], [o_pass * 100], s=180, marker="o",
                       facecolor=color, edgecolor=OMICVERSE_RING,
                       linewidths=2.4, zorder=5)
            ax.annotate(f"OmicVerse[{short_label(model_id)}]",
                        (o_cost, o_pass * 100),
                        textcoords="offset points", xytext=(7, 6),
                        fontsize=8.5, fontweight="bold", color="#222")
            ax.annotate("", xy=(o_cost, o_pass * 100),
                        xytext=(b_cost, b_pass * 100),
                        arrowprops=dict(arrowstyle="->", color=OMICVERSE_RING,
                                        alpha=0.55, lw=1.2,
                                        connectionstyle="arc3,rad=0.10"),
                        zorder=4)
            pts_for_pareto.append((o_cost, o_pass * 100))

    if pts_for_pareto:
        pts_for_pareto.sort()
        front, best = [], -1e9
        for c, s in pts_for_pareto:
            if s > best:
                front.append((c, s))
                best = s
        if front:
            ax.plot([c for c, _ in front], [s for _, s in front], "--",
                    color="#999999", lw=1.1, zorder=2, label="Pareto front")

    ax.set_xscale("log", base=10)
    ax.xaxis.set_major_locator(LogLocator(base=10))
    ax.xaxis.set_minor_locator(LogLocator(base=10, subs=tuple(range(2, 10))))
    ax.xaxis.set_major_formatter(FuncFormatter(
        lambda v, _: f"${v:g}" if v >= 1 else f"${v:.2f}"))
    ax.xaxis.set_minor_formatter(FuncFormatter(
        lambda v, _: (f"${v:g}" if v in (0.02, 0.05, 0.2, 0.5, 2, 5) else "")))
    ax.set_xlabel("Cost per task (USD, log scale, cache-adjusted)",
                  fontsize=11)
    ax.set_ylabel("Pass@1 (%)", fontsize=11)
    ax.set_title("OmicVerse-OmicBench — Pass@1 vs cost per task",
                 fontsize=12, fontweight="bold", pad=12)
    ax.grid(True, which="both", ls=":", alpha=0.45)

    handles, labels = ax.get_legend_handles_labels()
    from matplotlib.lines import Line2D
    handles.append(Line2D([0], [0], marker="o", color="w",
                          markerfacecolor="#999", markersize=10,
                          markeredgecolor="#444", label="baseline arm"))
    handles.append(Line2D([0], [0], marker="o", color="w",
                          markerfacecolor="#999", markersize=12,
                          markeredgecolor=OMICVERSE_RING, markeredgewidth=2.2,
                          label="OmicVerse[model]   +omicverse arm"))
    ax.legend(handles=handles, loc="lower right", fontsize=9, frameon=True,
              facecolor="white", edgecolor="#cccccc")

    fig.tight_layout()
    out = ANALYSIS / "cost_vs_score.png"
    fig.savefig(out, dpi=170, bbox_inches="tight")
    print(f"saved {out}")


if __name__ == "__main__":
    main()
